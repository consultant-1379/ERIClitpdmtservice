package com.ericsson.nms.dmt;

/**
 * Represents an item type collection child and provides specific information
 * about itself, for instance the allowed cardinality. It is used to represent
 * both regular collection children as well as reference collection children.
 */
public class ItemTypeCollectionChild extends ItemTypeChild {

	private Integer min;
	private Integer max;

	public Integer getMin() {
		return min;
	}

	public void setMin(Integer min) {
		this.min = min;
	}

	public Integer getMax() {
		return max;
	}

	public void setMax(Integer max) {
		this.max = max;
	}
}
